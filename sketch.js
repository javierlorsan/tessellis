    //rotate(int(random(4)) * PI);
    //img.fill(R.random_choice(cArr));
    img.fill(cArr[0]);
    img.rect(0, 0, w, h);
    img.fill(255);
    const offset = (min(w, h) - s) * R.random_num(0.1, 0.4) * 0;
    img.translate(offset, offset);
